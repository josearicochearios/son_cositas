public class metodo_de_java {
    public static void main(String[] args) {
        /// Metodo para obtener el numero minimo
        int a = Math.min(4, 5);
        /// Metodo para obtner un numero aleatorio entre 0.1 y 1;
        double numeroAleatorio = Math.random();

        /// Curiosidad: println tambien es un metodo que recibe un solo parametro
        System.out.println(a);
        System.out.println(numeroAleatorio);
    }
}

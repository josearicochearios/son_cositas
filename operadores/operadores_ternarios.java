public class operadores_ternarios {
    public static void main(String[] args) {
        boolean a = true;

        /// se evalua si el valor proporcionado es verdadero o falso, si es verdadero davuelve el valor antes de los dos
        /// puntos ( : ) si el valor es falso, devuelve el valor despues de los dos puntos
        /// es como un if else pero mas simple
        System.out.println(a ? "verdadero": "falso");
    }
}

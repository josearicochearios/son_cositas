public class operadores_comparacion {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        System.out.println("a es menor que b: " + (a < b)); /// se compara si b es mayor que a
        System.out.println("a es menor que b: " + (a > b)); /// se compara si a es mayor que b
        System.out.println("es igual: " + (a == b)); /// se compara si a y b son iguales
        System.out.println("es diferente: " + (a != b)); /// se compara si a y b no son iguales
    }
}
